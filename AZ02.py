import pandas as pd
import numpy as np

data = {
    'Имя': ['Анна', 'Борис', 'Вика', 'Глеб', 'Дина', 'Егор', 'Жанна', 'Зоя', 'Илья', 'Кирилл'],
    'Математика': [85, 90, 78, 92, 88, 76, 95, 89, 84, 91],
    'Физика': [80, 85, 79, 95, 83, 77, 92, 86, 81, 88],
    'Химия': [78, 82, 85, 91, 79, 73, 88, 90, 80, 87],
    'Биология': [88, 92, 84, 89, 90, 78, 91, 85, 83, 87],
    'История': [90, 85, 88, 93, 87, 80, 92, 89, 84, 91]
}

df = pd.DataFrame(data)

print("Первые строки таблицы:")
print(df.head())

print("\nСредняя оценка по каждому предмету:")
print(df.mean(numeric_only=True))

print("\nМедианная оценка по каждому предмету:")
print(df.median(numeric_only=True))

Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"\nQ1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")
print(f"IQR (межквартильный размах) по математике: {IQR_math}")

print("\nСтандартное отклонение по каждому предмету:")
print(df.std(numeric_only=True))
