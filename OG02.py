# Отправить изменения в онлайн-версию репозитория.
# Прикрепить ссылку на репозиторий.

import random
# Генерируем случайное число от 1 до 100
secret_number = random.randint(1, 100)
print("Компьютер загадал число от 1 до 100. У вас есть 5 попыток, чтобы его угадать!")

# Счетчик попыток
max_attempts = 5
attempts = 0

# Запускаем цикл игры
while attempts < max_attempts:
    try:
        # Пользователь вводит число
        guess = int(input(f"Попытка {attempts + 1} из {max_attempts}. Введите ваше предположение: "))
        attempts += 1  # Увеличиваем счетчик попыток

        # Проверяем угадал ли пользователь
        if guess < secret_number:
            print("Загаданное число больше.")
        elif guess > secret_number:
            print("Загаданное число меньше.")
        else:
            print(f"Поздравляем! Вы угадали число {secret_number} за {attempts} попыток.")
            break  # Число угадано, завершаем игру
    except ValueError:
        print("Ошибка: введите целое число.")
        continue  # Пропускаем попытку из-за некорректного ввода

# Если попытки закончились и число не угадано
if attempts == max_attempts and guess != secret_number:
    print(f"К сожалению, вы проиграли. Загаданное число было {secret_number}.")
